    <p>This is the website presence for the completely free standalone Windows Forms based diet monitoring application called Diet Sentry 2.0. Here you can download and install it onto your own Windows computer.</p>
    <p>It contains an Australian based foods database with around 1800 foods using the sensible units kJ, g, mg and mL, and the logical  d-MMM-yy HH:mm  DateTime format. It also contains selected commercial foods based on their mandated Nutritional Information Panels.</p>
    <p>When a food is consumed it is selected and an amount eaten entered. This appends the details to a DateTime stamped record. The cumulative totals of the daily Energy and nutrients can then be monitored.</p>
    <p>New foods can be manually added to the database. For convenience it is also possible to add a recipe to the foods database. Foods can also be deleted and edited, including eaten foods.</p>
    <p>The application source can be cloned into Visual Studio for compiling from the git repository <a href="https://github.com/namor5772/DietSentry">https://github.com/namor5772/DietSentry</a> </p>
